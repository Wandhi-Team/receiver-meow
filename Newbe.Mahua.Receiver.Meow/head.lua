--重写print函数
function print(...)
    if lua_run_result_var ~= "" then
        lua_run_result_var = lua_run_result_var.."\r\n"
    end
    for i=1,select('#', ...) do
        lua_run_result_var = lua_run_result_var..tostring(select(i, ...))
        if i ~= select('#', ...) then
            lua_run_result_var = lua_run_result_var.."\t"
        end
    end
end

--返回at某人的字符串
function at(qq)
    return "[CQ:at,qq="..tostring(qq).."]"
end

